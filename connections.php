									wp_redirect('admin.php?page=connections_templates&type=' . $tab . '&display_messages=true');
								break;
							}
						}
					}
					else
					{
						// @TODO: Create template specific error message.
						$connections->setErrorMessage('capability_settings');
					}
				break;
				
